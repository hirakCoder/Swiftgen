import asyncio
import aiohttp
import json
import time

async def test_food_delivery_app():
    """Test creating a DoorDash-style food delivery app"""
    
    url = "http://localhost:8000/api/generate"
    
    # Complex food delivery app description
    description = """Create a DoorDash-style food delivery app with:
    - Restaurant browsing with search and filters
    - Menu viewing with item details and customization
    - Shopping cart with quantity adjustment
    - User authentication and profile
    - Order tracking with real-time updates
    - Delivery address management
    - Payment integration placeholder
    - Rating and review system
    - Push notifications for order updates
    - Map view for delivery tracking"""
    
    payload = {
        "description": description,
        "ios_version": "16.0"
    }
    
    print("ğŸš€ Testing complex food delivery app generation...")
    print(f"ğŸ“ Description: {description[:100]}...")
    
    start_time = time.time()
    
    async with aiohttp.ClientSession() as session:
        try:
            # Send generation request
            async with session.post(url, json=payload) as response:
                if response.status == 200:
                    result = await response.json()
                    project_id = result.get('project_id')
                    print(f"âœ… Generation started with project ID: {project_id}")
                    
                    # Connect to WebSocket for progress updates
                    ws_url = f"ws://localhost:8000/ws/{project_id}"
                    async with session.ws_connect(ws_url) as ws:
                        print("ğŸ“¡ Connected to WebSocket for progress updates")
                        
                        async for msg in ws:
                            if msg.type == aiohttp.WSMsgType.TEXT:
                                data = json.loads(msg.data)
                                
                                # Print progress updates
                                if data.get('type') == 'status':
                                    print(f"[{data.get('status', 'unknown')}] {data.get('message', '')}")
                                
                                # Check for completion
                                if data.get('type') == 'complete':
                                    elapsed = time.time() - start_time
                                    print(f"\nğŸ‰ Generation completed in {elapsed:.1f} seconds!")
                                    print(f"ğŸ“Š Status: {data.get('status')}")
                                    print(f"ğŸ“± App Name: {data.get('app_name')}")
                                    print(f"ğŸ“ Files Generated: {data.get('files_count')}")
                                    print(f"âœ¨ Features: {', '.join(data.get('features', [])[:5])}")
                                    print(f"ğŸ¤– Generated by: {data.get('generated_by_llm')}")
                                    break
                                
                                # Check for errors
                                if data.get('type') == 'error':
                                    print(f"\nâŒ Error: {data.get('message')}")
                                    print(f"Details: {data.get('details')}")
                                    break
                            
                            elif msg.type == aiohttp.WSMsgType.ERROR:
                                print(f'WebSocket error: {ws.exception()}')
                                break
                else:
                    print(f"âŒ Failed to start generation: {response.status}")
                    error_text = await response.text()
                    print(f"Error: {error_text}")
                    
        except Exception as e:
            print(f"âŒ Exception: {str(e)}")

